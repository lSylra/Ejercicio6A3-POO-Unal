    private void BtnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCalcularActionPerformed
        float A = Float.parseFloat(TxtA.getText()); 
        float B = Float.parseFloat(TxtB.getText()); 
        float C = Float.parseFloat(TxtC.getText());
        
        float B2 = (float)Math.pow(B, 2);
        float X1,X2;
  
        X1 = (float)((-B+(Math.sqrt(B2-(4*A*C))))/(2*A));
        X2 = (float)((-B-(Math.sqrt(B2-(4*A*C))))/(2*A));
             
        String Valor = "Los valores de X son: "+X1+" y "+X2;
        TxtX.setText(Valor);
        
        JOptionPane.showMessageDialog(null,Valor,"Valor de X",JOptionPane.PLAIN_MESSAGE);               
    }//GEN-LAST:event_BtnCalcularActionPerformed

